static void cmd(uint8_t c) { i2c_write_reg(SSD1306_ADDR, 0x00, c); } /* 0x00 = command */
void ssd1306_init(void)
{
 static const uint8_t seq[] = {
 0xAE, /* display off */
 0x20, 0x00, /* memory addressing mode = horizontal */
 0x40, /* start line 0 */
 0xA1, /* segment remap (left-right) */
 0xC8, /* COM scan direction (top-bottom) */
 0xA8, 0x3F, /* multiplex ratio = 63 (for 64 rows) */
 0xD3, 0x00, /* display offset 0 */
 0xDA, 0x12, /* COM pins config (0x12 for 128x64) */
 0xD5, 0x80, /* clock divide / osc freq */
 0xD9, 0xF1, /* pre-charge period */
 0xDB, 0x40, /* VCOMH deselect level */
 0x81, 0x7F, /* contrast */
 0xA4, /* resume from RAM (not all-on) */
 0xA6, /* normal (not inverted) */
 0x8D, 0x14, /* CHARGE PUMP ON (or the screen stays black) */
 0xAF /* display on */
 };
 for (uint32_t i = 0; i < sizeof seq; i++) cmd(seq[i]);
 ssd1306_clear();
 ssd1306_flush();
}
void ssd1306_clear(void) { memset(fb, 0x00, sizeof fb); }
/* Draw an ASCII string from pixel column `col`, text row `page` (0..7).
 * The 8x8 font carries its own spacing, so characters sit flush. */
void ssd1306_draw_string(uint8_t col, uint8_t page, const char *s)
{
 if (page >= PAGES) return;
 while (*s && (uint16_t)col + CW <= W) {
 char ch = *s++;
 if (ch < 0x20 || ch > 0x7E) ch = '?';
 const uint8_t *g = font8x8[ch - 0x20];
 for (int i = 0; i < CW; i++) fb[page * W + col++] = g[i];
 }
}
/* Set the full window, then stream all 1024 framebuffer bytes in one write. */
void ssd1306_flush(void)
{
 cmd(0x21); cmd(0); cmd(W - 1); /* column range 0..127 */
 cmd(0x22); cmd(0); cmd(PAGES - 1); /* page range 0..7 */
 i2c_write_buf(SSD1306_ADDR, 0x40, fb, sizeof fb); /* 0x40 = pixel data */
}
